void main(List<String> args) {
  // Arithmetic Operators (+, -, *, /, %, ++, --)
  int a = 10;
  int b = 20;
  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);
  print(a++);
  print(a--);

  print("\n");

  // Relational Operators (==, !=, >, <, >=, <=)
  print(a == b);
  print(a != b);
  print(a > b);
  print(a < b);
  print(a >= b);
  print(a <= b);

  print("\n");

  // Logical Operators (&&, ||, !)
  bool x = true;
  bool y = false;
  print(x && y);
  print(x);
  print(!x);
}
